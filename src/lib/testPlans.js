// בונה את כל תכנית הבדיקות כטקסט פשוט לשמירה ב-Drive
export function buildPlanText() {
  const L = [];
  L.push('תכנית בדיקות — מערכת CRM ובוט WhatsApp | קרנות ראמים');
  L.push('נוצר: ' + new Date().toLocaleString('he-IL'));
  L.push('');
  L.push('הערות כלליות:');
  L.push('• שלב שורנס (שאלון) עדיין בפיתוח — דלגו עליו בינתיים.');
  L.push('• ייבוא אקסלים לא נכלל בבדיקות (טרם נבנה).');
  L.push('• בבדיקות הבוט — נקו רשומות לפני כל סבב כדי לדמות משתמש חדש.');
  L.push('');
  L.push('==================================================');
  L.push('חלק 1 — בדיקות מערכת ה-CRM (אדמין / בשמת)');
  L.push('==================================================');
  CRM_GROUPS.forEach((g, gi) => {
    L.push('');
    L.push(`${gi + 1}. ${g.title}`);
    L.push('   ' + g.intro);
    g.steps.forEach((s, i) => {
      L.push(`   ${gi + 1}.${i + 1} [${s.where}] ${s.action}`);
      L.push(`        ← מצופה: ${s.expect}`);
    });
  });

  L.push('');
  L.push('==================================================');
  L.push('חלק 2 — בדיקות הבוט: פניות שירות (משתמש קצה)');
  L.push('==================================================');
  L.push(BOT_SERVICE_FLOW.intro);
  L.push(BOT_SERVICE_FLOW.note);
  SERVICE_TYPES.forEach((t, ti) => {
    L.push('');
    L.push(`-- סוג שירות ${ti + 1}: ${t.label} --`);
    BOT_SERVICE_FLOW.steps.forEach((s, i) => {
      L.push(`   ${i + 1}. [${s.where}] ${s.action}`);
      L.push(`        ← מצופה: ${s.expect}`);
    });
  });

  L.push('');
  L.push('==================================================');
  L.push('חלק 3 — בדיקות הבוט: נתיב וובינר (משתמש קצה)');
  L.push('==================================================');
  L.push(BOT_WEBINAR_FLOW.intro);
  BOT_WEBINAR_FLOW.steps.forEach((s, i) => {
    L.push(`   ${i + 1}. [${s.where}] ${s.action}`);
    L.push(`        ← מצופה: ${s.expect}`);
  });

  return L.join('\n');
}